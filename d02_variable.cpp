#include <stdio.h>

int main() {
	printf("Hello, Welcome to C Language! \n"); // \n: ky hieu dieu khien xuong dong

	//khai bao bien bo nho ten [a], kieu so nguyen, va gan cho bien a = 100
	int a = 100;

	//khai bao bien bo nho ten [b], kieu so thuc-6 so le, va gan b = 3.1415
	float b = 3.1415;

	//khai bao bien bo nho ten [c], kieu ky tu, va gan c = '@'
	char c ='@';


	//in noi dung cua 3 bien [a], [b] va [c] ra man hinh
	printf("%d la gia tri cua bien a\n" , a);
	printf("b = %f \n" , b);
	printf("c = %c \n" , c);

}
